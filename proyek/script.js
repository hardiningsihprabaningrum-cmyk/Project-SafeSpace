    function pickAns(i) {
        if (answered) return;
        answered = true;
        clearInterval(quizTimer);
        qAns[qIdx] = i;
        
        var q = quizQ[qIdx];
        if (i === q.c) {
            correctCount++;
            streak++;
            if (streak > maxStreak) maxStreak = streak;
        } else {
            incorrectCount++;
            streak = 0;
        }
        
        renderQ();
        setTimeout(nextQuestionOrFinish, 2000);
    }
    
    function nextQuestionOrFinish() {
        if (qIdx < quizQ.length - 1) {
            qIdx++;
            startTimer();
            renderQ();
        } else {
            qDone = true;
            showQResult();
        }
    }

