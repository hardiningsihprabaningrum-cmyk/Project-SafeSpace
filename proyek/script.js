    function startConfetti() {
        var canvas = document.getElementById('confettiCanvas');
        if (!canvas) return;
        var ctx = canvas.getContext('2d');
        canvas.width = canvas.offsetWidth || 440;
        canvas.height = canvas.offsetHeight || 520;
        var colors = ['#3A72E8','#10B981','#FF6B4A','#7C3AED','#F59E0B','#EC4899'];
        var pieces = [];
        for (var i = 0; i < 80; i++) {
            pieces.push({
                x: Math.random() * canvas.width,
                y: Math.random() * canvas.height - canvas.height,
                w: Math.random() * 10 + 5,
                h: Math.random() * 6 + 3,
                color: colors[Math.floor(Math.random() * colors.length)],
                rot: Math.random() * Math.PI * 2,
                vx: (Math.random() - .5) * 2,
                vy: Math.random() * 3 + 1.5,
                vrot: (Math.random() - .5) * .12
            });
        }
        var frames = 0;
        function draw() {
            ctx.clearRect(0, 0, canvas.width, canvas.height);
            pieces.forEach(function(p) {
                ctx.save();
                ctx.translate(p.x + p.w/2, p.y + p.h/2);
                ctx.rotate(p.rot);
                ctx.fillStyle = p.color;
                ctx.globalAlpha = Math.max(0, 1 - frames / 90);
                ctx.fillRect(-p.w/2, -p.h/2, p.w, p.h);
                ctx.restore();
                p.x += p.vx; p.y += p.vy; p.rot += p.vrot;
            });
            frames++;
            if (frames < 100) requestAnimationFrame(draw);
            else ctx.clearRect(0, 0, canvas.width, canvas.height);
        }
        draw();
    }

