    function exportPDF() {
        if (typeof window.jspdf === 'undefined') { toast('Library PDF belum dimuat. Coba lagi nanti.', 'err'); return; }
        var jsPDF = window.jspdf.jsPDF; var doc = new jsPDF();
        doc.setFont('helvetica', 'bold'); doc.setFontSize(20); doc.text('SafeSpace - Riwayat Laporan', 14, 22);
        doc.setFont('helvetica', 'normal'); doc.setFontSize(10); doc.text('Diekspor pada: ' + new Date().toLocaleString('id-ID'), 14, 30); doc.text('Total Laporan: ' + reports.length, 14, 36);
        var y = 46;
        for (var i = 0; i < reports.length; i++) {
            var r = reports[i]; if (y > 260) { doc.addPage(); y = 20; }
            doc.setFont('helvetica', 'bold'); doc.setFontSize(11); doc.text((i + 1) + '. ' + r.type + ' - ' + r.location, 14, y); y += 6;
            doc.setFont('helvetica', 'normal'); doc.setFontSize(9); doc.text('Pelapor: ' + r.name + '  |  Status: ' + r.status + '  |  Tanggal: ' + r.date, 14, y); y += 5;
            var desc = r.description.length > 100 ? r.description.substring(0, 100) + '...' : r.description; doc.text('Deskripsi: ' + desc, 14, y); y += 5;
            if(r.tindakLanjut) { var tl = r.tindakLanjut.length > 100 ? r.tindakLanjut.substring(0, 100) + '...' : r.tindakLanjut; doc.setTextColor(58, 114, 232); doc.text('Tindak Lanjut: ' + tl, 14, y); doc.setTextColor(0, 0, 0); y += 5; }
            y += 8;
        }
        doc.save('SafeSpace_Laporan.pdf'); toast('Laporan berhasil diekspor ke PDF!', 'suc');
    }

