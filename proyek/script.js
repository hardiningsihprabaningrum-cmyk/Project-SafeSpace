    // PERBAIKAN: Menangani jika user klik Batal, dropdown kembali ke semula
    function closeTindakMod() { 
        document.getElementById('tindakMod').classList.remove('open'); 
        document.body.style.overflow = ''; 
        
        // Cek apakah laporan yang dimaksud statusnya belum berubah menjadi Ditindaklanjuti
        // Jika belum, kembalikan dropdown select ke status sebelumnya
        var id = document.getElementById('tindakId').value;
        var report = reports.find(function(r) { return r.id === id; });
        if (report && report.status !== 'Ditindaklanjuti') {
            var selectEl = document.querySelector('#rpt-' + id + ' select');
            if (selectEl) selectEl.value = report.status;
        }
        
        renderHist(); // Render ulang daftar setelah modal ditutup
    }

